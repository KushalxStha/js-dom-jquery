// Operators

// 01. Arithmetic Operators
let a = 10;
let b = 4;

console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a ** b =", a ** b);
console.log("a % b =", a % b);
console.log("a++ =", a++);
console.log("a-- =", a--);
console.log("--a =", --a);
console.log("++a =", ++a);


// 02. Assignment Operators
let as = 2;
console.log("as =",as);

console.log("as += 5:",as+=5);
console.log("as -= 5:",as-=5);
console.log("as *= 5:",as*=5);
console.log("as /= 5:",as/=5);
console.log("as **= 5:",as**=5);


// 03. Comparison Operators

let a1=6;
let b1=7;

console.log("a1 == b1 is ",a1==b1);
console.log("a1 != b1 is ",a1!=b1);
console.log("a1 === b1 is ",a1===b1);   // equal value & same data type
console.log("a1 !== b1 is ",a1!==b1);   // not equal value OR not same data type


// 04. Logical Operators

let l1 = 5;
let l2 = 6;

console.log(l1<l2 && l2==6);
console.log(l1>l2 || l2==6);
